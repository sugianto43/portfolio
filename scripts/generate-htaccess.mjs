// Generates public/.htaccess — the Apache equivalent of next.config.mjs's headers(),
// which only runs on a Node server and is skipped entirely for STATIC_EXPORT builds.
// Run automatically by `pnpm run build:static`, before `next build`. Edit
// lib/security-headers.mjs, not this script's output, to change header values.
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { buildSecurityHeaders, cacheableAssetExtensions } from "../lib/security-headers.mjs";

const securityHeaders = buildSecurityHeaders(false);
const extensionPattern = cacheableAssetExtensions.join("|");

const headerLines = securityHeaders
  .map(({ key, value }) => `  Header always set ${key} "${value}"`)
  .join("\n");

const htaccess = `# Generated by scripts/generate-htaccess.mjs from lib/security-headers.mjs — do not edit
# by hand, edit the source and re-run \`pnpm run build:static\`.
#
# Apache equivalent of the security headers normally set by next.config.mjs's
# headers() function, which only runs on a Node server — not on static hosting
# (e.g. Hostinger shared hosting).

# Next's custom 404 page (app/not-found.tsx), exported as out/404.html.
ErrorDocument 404 /404.html

<IfModule mod_headers.c>
${headerLines}
</IfModule>

<IfModule mod_expires.c>
  ExpiresActive On
  <FilesMatch "\\.(${extensionPattern})$">
    ExpiresDefault "access plus 1 year"
  </FilesMatch>
</IfModule>

<IfModule mod_headers.c>
  <FilesMatch "\\.(${extensionPattern})$">
    Header set Cache-Control "public, max-age=31536000, immutable"
  </FilesMatch>
</IfModule>

# app/icon.tsx and app/opengraph-image.tsx export as extensionless files
# (out/icon, out/opengraph-image), so the FilesMatch extension rules above never
# match them — they need their own Content-Type and cache rules.
<IfModule mod_mime.c>
  <Files "icon">
    ForceType image/png
  </Files>
  <Files "opengraph-image">
    ForceType image/png
  </Files>
</IfModule>
<IfModule mod_headers.c>
  <FilesMatch "^(icon|opengraph-image)$">
    Header set Cache-Control "public, max-age=31536000, immutable"
  </FilesMatch>
</IfModule>

# Next static export writes pages as page/index.html (trailingSlash: true in
# next.config.mjs); serve them without the trailing /index.html so links like
# "/about" keep working.
<IfModule mod_rewrite.c>
  RewriteEngine On
  RewriteCond %{REQUEST_FILENAME} !-f
  RewriteCond %{REQUEST_FILENAME}/index.html -f
  RewriteRule ^(.*)$ $1/index.html [L]
</IfModule>
`;

const outPath = fileURLToPath(new URL("../public/.htaccess", import.meta.url));
writeFileSync(outPath, htaccess);
console.log(`Wrote ${outPath}`);
